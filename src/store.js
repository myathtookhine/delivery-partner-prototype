const useStore = create((set) => ({
  pickups: initialPickups,
  packages: initialPackages,
  setPickups: (pickups) => set({ pickups }),
  updatePickupStatus: (pickupId, status) =>
    set((state) => {
      const updatedPickups = state.pickups.map((p) => {
        if (p.batchNo === pickupId) {
          const updatedPickup = { ...p, status };
          if (status === "delivered") {
            updatedPickup.completedAt = new Date().toISOString();
          }
          if (status === "delivering") {
            updatedPickup.startedDeliveryAt = new Date().toISOString();
          }
          return updatedPickup;
        }
        return p;
      });
      return { pickups: updatedPickups };
    }),

  // Currently delivering batch (only one at a time)
  activeDeliveryBatchNo: null,
  setActiveDeliveryBatchNo: (batchNo) =>
    set({ activeDeliveryBatchNo: batchNo }),
}));

export default useStore;
